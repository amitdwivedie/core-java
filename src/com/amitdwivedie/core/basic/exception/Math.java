package com.amitdwivedie.core.basic.exception;

class Math {
    public static int addition(int i, int j) {
        return (i + j);
    }

    public static int substraction(int i, int j) {
        return (i - j);
    }

    public static int multiplication(int i, int j) {
        return (i * j);
    }

    public static int division(int i, int j) {
        return (i / j);
    }

}